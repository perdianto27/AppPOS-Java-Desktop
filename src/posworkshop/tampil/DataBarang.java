    protected void datatable(){
        Object[] Baris ={"Kode Barang","Nama Barang","Jenis Barang","Harga Beli","Harga Jual","Stok Barang"};
        tabmode = new DefaultTableModel(null, Baris);
        tblBrg.setModel(tabmode);
        String sql= "select * from barang";
    try{
        java.sql.Statement stat=conn.createStatement();
        ResultSet hasil=stat.executeQuery(sql);
        while(hasil.next()){
            String kode=hasil.getString("kd_brg");
            String nama=hasil.getString("nama_brg");
            String jenis=hasil.getString("jenis_brg");
            String hargabeli=hasil.getString("harga_beli");
            String hargajual=hasil.getString("harga_jual");
            String stok=hasil.getString("stok_brg");
            String[] data={kode,nama,jenis,hargabeli,hargajual,stok};
            tabmode.addRow(data);
    }
    } catch (Exception e) {
        }
    }


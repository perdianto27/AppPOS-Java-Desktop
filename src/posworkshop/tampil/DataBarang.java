    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String sql="insert into barang values(?,?,?,?,?,?)";
        try{
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtkd.getText());
        stat.setString(2, txtnm.getText());
        stat.setString(3, txtjns.getText());
        stat.setString(4, txthrgbl.getText());
        stat.setString(5, txthrgjl.getText());
        stat.setString(6, txtstk.getText());
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Berhasil Simpan");
        kosong();
        txtkd.requestFocus();
        datatable();
        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan"+e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tblBrgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBrgMouseClicked
        // TODO add your handling code here:
        try{
            int row=tblBrg.getSelectedRow();
            String tabel_klik=(tblBrg.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet sql=stm.executeQuery("SELECT * FROM barang where kd_brg='"+tabel_klik+"'");
            if(sql.next()){
                String kode=sql.getString("kd_brg");
                    txtkd.setText(kode);
                String nama=sql.getString("nama_brg");
                    txtnm.setText(nama);
                String jenis=sql.getString("jenis_brg");
                    txtjns.setText(jenis);
                String hargabeli=sql.getString("harga_beli");
                    txthrgbl.setText(hargabeli);
                String hargajual=sql.getString("harga_jual");
                    txthrgjl.setText(hargajual);
                String stokbarang=sql.getString("stok_brg");
                    txtstk.setText(stokbarang);                    
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tidak ada yang anda pilih");
        }
    }//GEN-LAST:event_tblBrgMouseClicked

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        String sql="UPDATE barang SET nama_brg=?,jenis_brg=?, harga_beli=?, harga_jual=?, stok_brg=? where kd_brg='"+txtkd.getText()+"'";
        try{
            PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1, txtnm.getText());
            stat.setString(2, txtjns.getText());
            stat.setString(3, txthrgbl.getText());
            stat.setString(4, txthrgjl.getText());
            stat.setString(5, txtstk.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            txtkd.requestFocus();
            datatable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Simpan"+e);
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null,"Apakah anda yakin ingin menghapus data", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if(ok==0){
            String sql="DELETE FROM barang WHERE kd_brg='"+txtkd.getText()+"'";
            try{
                PreparedStatement stat=conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                kosong();
                txtkd.requestFocus();
                datatable();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null,"Data Gagal Dihapus" +e);
            }
    }
    }//GEN-LAST:event_btnHapusActionPerformed



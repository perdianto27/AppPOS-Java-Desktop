    protected void datatable(){
        Object[] Baris ={"Kode Masuk","Tanggal Masuk","Nomor Pembelian","Tanggal Pembelian","Kode Barang","Nama Barang","Jenis","Jumlah"};
        tabmode = new DefaultTableModel(null, Baris);
        tblBrgMasuk.setModel(tabmode);
        String sql= "select * from barang_masuk";
    try{
        java.sql.Statement stat=conn.createStatement();
        ResultSet hasil=stat.executeQuery(sql);
        while(hasil.next()){
            String kodemasuk=hasil.getString("kd_brg_masuk");
            String tanggalmasuk=hasil.getString("tgl_brg_masuk");
            String nomorbeli=hasil.getString("no_pembelian");
            String tanggalbeli=hasil.getString("tgl_pembelian");
            String kode=hasil.getString("kd_brg");
            String nama=hasil.getString("nama_brg");
            String jenis=hasil.getString("jenis");
            String jumlah=hasil.getString("jumlah");
            String[] data={kodemasuk,tanggalmasuk,nomorbeli,tanggalbeli,kode,nama,jenis,jumlah};
            tabmode.addRow(data);
    }
    } catch (Exception e) {
        }
    }  

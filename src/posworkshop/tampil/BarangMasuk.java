    private void tblBrgMasukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBrgMasukMouseClicked
        // TODO add your handling code here:
        try{
            int row=tblBrgMasuk.getSelectedRow();
            String tabel_klik=(tblBrgMasuk.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet sql=stm.executeQuery("SELECT * FROM barang_masuk where kd_brg_masuk='"+tabel_klik+"'");
            if(sql.next()){
                String kodemasuk=sql.getString("kd_brg_masuk");
                    txtkdbrgm.setText(kodemasuk);
                String tanggalmasuk=sql.getString("tgl_brg_masuk");
                    txttglm.setText(tanggalmasuk);
                String nomorpembelian=sql.getString("no_pembelian");
                    txtnobl.setText(nomorpembelian);
                String tanggalbeli=sql.getString("tgl_pembelian");
                    txttglbl.setText(tanggalbeli);
                String kode=sql.getString("kd_brg");
                    txtkd.setText(kode);
                String nama=sql.getString("nama_brg");
                    txtnm.setText(nama);
                String jenis=sql.getString("jenis");
                    txtjns.setText(jenis);
                String jumlah=sql.getString("jumlah");
                    txtjml.setText(jumlah);                    
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tidak ada yang anda pilih");
        }        
    }//GEN-LAST:event_tblBrgMasukMouseClicked


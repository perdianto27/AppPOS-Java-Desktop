    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String sql="insert into barang_masuk values(?,?,?,?,?,?,?,?)";
        try{
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, txtkdbrgm.getText());
        stat.setString(2, txttglm.getText());
        stat.setString(3, txtnobl.getText());
        stat.setString(4, txttglbl.getText());
        stat.setString(5, txtkd.getText());
        stat.setString(6, txtnm.getText());
        stat.setString(7, txtjns.getText());
        stat.setString(8, txtjml.getText());
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Berhasil Simpan");
        kosong();
        txtkdbrgm.requestFocus();
        datatable();
        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan"+e);
        }        
    }//GEN-LAST:event_btnSimpanActionPerformed

